package pacotecalculadora;

public class CalculadoraINT {
    public int soma(int a, int b) {
        System.out.println("SOMA INT " + a + " + " + b);
        return a + b;
    }

    public int multiplica(int a, int b) {
        System.out.println("MULT INT " + a + " * " + b);
        return a * b;
    }
}