package pacotecalculadora ;
public class CalculadoraFP {
    public float soma(float a, float b) {
        System.out.println("SOMA FP " + a + " + " + b);
        return a + b;
    }

    public float multiplica(float a, float b) {
        System.out.println("MULT FP " + a + " * " + b);
        return a * b;
    }
}